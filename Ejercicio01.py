print("Temperatura promedio: Fin de semana en Arequipa (en C°)")

viernes=float(input("Ingrese la temperatura del viernes: "))
sabado=float(input("Ingrese la temperatura del sábado: "))
domingo=float(input("Ingrese la temperatura del domingo: "))

promedio=(viernes+sabado+domingo)/3

print("La temperatura promedio del fin de semana en Arequipa es:", promedio, "°C")